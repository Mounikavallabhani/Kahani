package com.arkainfoteck.kahani.Model;

public class TopSeries {
    int topstoriesimage;
    String topstoriesheadding;
    String topstoriesname;
    String topstoriestime;
    String topstoriesviews;
    String topstoriessubsribe;

    public TopSeries(int topstoriesimage, String topstoriesheadding, String topstoriesname, String topstoriestime, String topstoriesviews, String topstoriessubsribe) {
        this.topstoriesimage = topstoriesimage;
        this.topstoriesheadding = topstoriesheadding;
        this.topstoriesname = topstoriesname;
        this.topstoriestime = topstoriestime;
        this.topstoriesviews = topstoriesviews;
        this.topstoriessubsribe = topstoriessubsribe;
    }

    public int getTopstoriesimage() {
        return topstoriesimage;
    }

    public void setTopstoriesimage(int topstoriesimage) {
        this.topstoriesimage = topstoriesimage;
    }

    public String getTopstoriesheadding() {
        return topstoriesheadding;
    }

    public void setTopstoriesheadding(String topstoriesheadding) {
        this.topstoriesheadding = topstoriesheadding;
    }

    public String getTopstoriesname() {
        return topstoriesname;
    }

    public void setTopstoriesname(String topstoriesname) {
        this.topstoriesname = topstoriesname;
    }

    public String getTopstoriestime() {
        return topstoriestime;
    }

    public void setTopstoriestime(String topstoriestime) {
        this.topstoriestime = topstoriestime;
    }

    public String getTopstoriesviews() {
        return topstoriesviews;
    }

    public void setTopstoriesviews(String topstoriesviews) {
        this.topstoriesviews = topstoriesviews;
    }

    public String getTopstoriessubsribe() {
        return topstoriessubsribe;
    }

    public void setTopstoriessubsribe(String topstoriessubsribe) {
        this.topstoriessubsribe = topstoriessubsribe;
    }
}

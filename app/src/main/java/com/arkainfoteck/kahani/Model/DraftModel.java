package com.arkainfoteck.kahani.Model;

public class DraftModel {
    String title;
    String content;

    public DraftModel(String title, String content) {
        this.title = title;
        this.content = content;
    }

    public String getTitle() {
        return title;
    }

    public String getContent() {
        return content;
    }


}
